#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << pow(2,5)    << endl;
    cout << sqrt(36)    << endl;
    cout << round(4.36) << endl;
    cout << ceil(4.2)   << endl;
    cout << floor(6.8)  << endl;
    cout << fmax(4,5)   << endl;
    cout << fmin(4,5)   << endl;
    return 0;
}
